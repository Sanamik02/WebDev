s = input()

# 1. Third character
print(s[2])

# 2. Second-to-last character
print(s[-2])

# 3. First 5 characters
print(s[:5])

# 4. All characters except last 2
print(s[:-2])

# 5. Characters at even indices
print(s[::2])

# 6. Characters at odd indices
print(s[1::2])

# 7. All characters in reverse order
print(s[::-1])

# 8. All characters in reverse order, stepping by 1
print(s[::-2])

# 9. Length of the string
print(len(s))